<b>Article 2 - Cookie stockant des informations fournies par la personne concernée</b><br>
[MetalLinK].<br>
MetalLink.fr implante un cookie dans votre ordinateur.<br>
Ce cookie enregistre des informations relatives à votre navigation sur notre site, et stocke des informations que vous avez saisies durant votre visite : login & mot de passe.<br>

Ainsi, vous n'aurez pas besoin, lors de votre prochaine visite, de les saisir à nouveau. Nous pourrons les consulter lors de vos prochaines visites.<br>

La durée de conservation de ces informations dans votre ordinateur est de 1 an.<br>

Nous vous informons que vous pouvez vous opposer à l'enregistrement de "cookies" en configurant votre navigateur de la manière suivante : <br>
Pour Mozilla firefox :
<br><br>
   1. Choisissez le menu "outil " puis "Options"
   2. Cliquez sur l'icône "vie privée"
   3. Repérez le menu "cookie" et sélectionnez les options qui vous conviennent
<br><br>
Pour Microsoft Internet Explorer  :
<br><br>
   1. choisissez le menu "Outils" (ou "Tools"), puis "Options Internet" (ou "Internet Options"). 
   2. cliquez sur l'onglet "Confidentialité" (ou "Confidentiality") 
   3. sélectionnez le niveau souhaité à l'aide du curseur. 
<br><br>
Pour Opéra :
<br><br>
   1. choisissez le menu "Fichier">"Préférences"
   2. Vie Privée
